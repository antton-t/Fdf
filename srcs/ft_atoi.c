#include "ft_fdf.h"

int	ft_atoi(char **line)
{
	int	sign;
	int	nb;

	nb = 0;
	sign = 1;
	if ((**line) == '-' || (**line) == '+')
	{
		if ((**line) == '-')
			sign = -1;
		(*line)++;
	}
	while ((**line) >= '0' && (**line) <= '9')
	{
		nb = nb * 10 + ((**line) - '0');
		(*line)++;
	}
	return (sign * nb);
}
